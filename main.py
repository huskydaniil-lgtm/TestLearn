    conn = get_db()
    try:
        results = conn.execute("""
            SELECT qr.id, q.title as quiz_title, qr.score, qr.total,
                   ROUND(CAST(qr.score AS FLOAT) / qr.total * 100, 1) as percentage,
                   qr.created_at
            FROM quiz_results qr
            JOIN quizzes q ON qr.quiz_id = q.id
            ORDER BY qr.created_at DESC
        """).fetchall()
    finally:
        conn.close()

    if not results:
        return PlainTextResponse(
            "Нет результатов для экспорта",
            media_type="text/plain; charset=utf-8"
        )

    # Создаем PDF
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(buffer, pagesize=A4)
    styles = getSampleStyleSheet()
    story = []

    # Заголовок
    title_style = ParagraphStyle(
        'CustomTitle',
        parent=styles['Heading1'],
        fontSize=24,
        spaceAfter=30,
        alignment=1  # center
    )
    story.append(Paragraph("Отчет по результатам тестирования", title_style))
    story.append(Spacer(1, 20))

    # Информация о дате экспорта
    date_style = ParagraphStyle(
        'DateStyle',
        parent=styles['Normal'],
        fontSize=10,
        alignment=1,
        spaceAfter=20
    )
    story.append(Paragraph(f"Дата экспорта: {datetime.now().strftime('%d.%m.%Y %H:%M')}", date_style))
    story.append(Spacer(1, 20))

    # Таблица результатов
    data = [["ID", "Тест", "Баллы", "Всего", "Процент", "Дата"]]
    for r in results:
        data.append([
            str(dict(r)["id"]),
            str(dict(r)["quiz_title"]),
            str(dict(r)["score"]),
            str(dict(r)["total"]),
            f"{dict(r)['percentage']}%",
            str(dict(r)["created_at"])
        ])

    table = Table(data)
    table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.grey),
        ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
        ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
        ('FONTSIZE', (0, 0), (-1, 0), 12),
        ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
        ('BACKGROUND', (0, 1), (-1, -1), colors.beige),
        ('GRID', (0, 0), (-1, -1), 1, colors.black)
    ]))
    
    story.append(table)
    story.append(Spacer(1, 30))

    # Статистика
    if results:
        scores = [dict(r)["score"] / dict(r)["total"] * 100 for r in results]
        avg_score = sum(scores) / len(scores)
        max_score = max(scores)
        min_score = min(scores)
        
        stats_data = [
            ["Статистика:", ""],
            ["Средний балл", f"{avg_score:.1f}%"],
            ["Максимальный балл", f"{max_score:.1f}%"],
            ["Минимальный балл", f"{min_score:.1f}%"],
            ["Всего попыток", str(len(results))]
        ]
        
        stats_table = Table(stats_data, colWidths=[200, 100])
        stats_table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.lightblue),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.black),
            ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, -1), 10),
            ('GRID', (0, 0), (-1, -1), 1, colors.black)
        ]))
        
        story.append(stats_table)

    doc.build(story)
    buffer.seek(0)
    
    return StreamingResponse(
        io.BytesIO(buffer.read()),
        media_type="application/pdf",
        headers={"Content-Disposition": "attachment; filename=testlearn_results.pdf"}
    )


