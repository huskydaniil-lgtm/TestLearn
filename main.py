@app.get("/admin/api/glossary")
async def admin_get_glossary(letter: Optional[str] = None, search: Optional[str] = None):
    """API: Получить все термины глоссария для админ-панели с опциональной фильтрацией."""
    conn = get_db()
    try:
        if search:
            search_pattern = f"%{search}%"
            terms = conn.execute("""
                SELECT * FROM glossary
                WHERE term LIKE ? OR definition LIKE ?
                ORDER BY letter, term
            """, (search_pattern, search_pattern)).fetchall()
        elif letter:
            terms = conn.execute("""
                SELECT * FROM glossary WHERE letter = ? ORDER BY term
            """, (letter,)).fetchall()
        else:
            terms = conn.execute("""
                SELECT * FROM glossary ORDER BY letter, term
            """).fetchall()
    finally:
        conn.close()
    return [dict(t) for t in terms]


