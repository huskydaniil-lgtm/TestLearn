@app.post("/admin/api/glossary")
async def admin_create_glossary_term(
    term: str = Form(...),
    definition: str = Form(...),
    letter: str = Form(...)
):
    """API: Создать новый термин глоссария."""
    conn = get_db()
    try:
        # Проверяем уникальность термина
        existing = conn.execute(
            "SELECT id FROM glossary WHERE term = ?",
            (term,)
        ).fetchone()
        if existing:
            raise HTTPException(status_code=400, detail="Термин уже существует")
        
        # Проверяем, что буква - один символ
        if len(letter) != 1 or not letter.isalpha():
            raise HTTPException(status_code=400, detail="Буква должна быть одним символом из алфавита")
        
        cursor = conn.execute(
            "INSERT INTO glossary (term, definition, letter) VALUES (?, ?, ?)",
            (term, definition, letter.upper())
        )
        term_id = cursor.lastrowid
        conn.commit()
    finally:
        conn.close()
    
    return {"id": term_id, "message": "Термин глоссария успешно создан"}


@app.put("/admin/api/glossary/{term_id}")
async def admin_update_glossary_term(
    term_id: int,
    term: str = Form(...),
    definition: str = Form(...),
    letter: str = Form(...)
):
    """API: Обновить термин глоссария."""
    conn = get_db()
    try:
        # Проверяем существование термина
        existing_term = conn.execute(
            "SELECT id FROM glossary WHERE id = ?",
            (term_id,)
        ).fetchone()
        if not existing_term:
            raise HTTPException(status_code=404, detail="Термин не найден")
        
        # Проверяем уникальность термина (исключая текущий термин)
        duplicate = conn.execute(
            "SELECT id FROM glossary WHERE term = ? AND id != ?",
            (term, term_id)
        ).fetchone()
        if duplicate:
            raise HTTPException(status_code=400, detail="Термин уже существует")
        
        # Проверяем, что буква - один символ
        if len(letter) != 1 or not letter.isalpha():
            raise HTTPException(status_code=400, detail="Буква должна быть одним символом из алфавита")
        
        conn.execute(
            "UPDATE glossary SET term = ?, definition = ?, letter = ? WHERE id = ?",
            (term, definition, letter.upper(), term_id)
        )
        conn.commit()
    finally:
        conn.close()
    
    return {"message": "Термин глоссария успешно обновлен"}


