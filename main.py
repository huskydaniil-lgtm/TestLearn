def create_admin_session(username: str) -> str:
    """Создать новую сессию для администратора."""
    session_id = str(uuid.uuid4())
    expires = datetime.now() + timedelta(hours=24)
    conn = get_db()
    try:
        conn.execute(
            "INSERT INTO admin_sessions (id, username, expires, created_at) VALUES (?, ?, ?, ?)",
            (session_id, username, expires.isoformat(), datetime.now().isoformat())
        )
        conn.commit()
    finally:
        conn.close()
    return session_id


def verify_admin_session(request: Request) -> bool:
    """Проверить, существует ли действительная сессия администратора."""
    session_id = request.cookies.get("admin_session")
    if not session_id:
        return False
    conn = get_db()
    try:
        cursor = conn.execute(
            "SELECT username, expires FROM admin_sessions WHERE id = ?",
            (session_id,)
        )
        row = cursor.fetchone()
        if not row:
            return False
        expires = datetime.fromisoformat(row["expires"])
        if expires < datetime.now():
            # Сессия истекла, удаляем её
            conn.execute("DELETE FROM admin_sessions WHERE id = ?", (session_id,))
            conn.commit()
            return False
        return True
    finally:
        conn.close()


def get_current_admin(request: Request):
    """Dependency для защиты маршрутов админ-панели."""
    if not verify_admin_session(request):
        raise HTTPException(status_code=401, detail="Not authenticated")
    return True


def init_db():
    """Инициализация таблиц БД."""
    conn = get_db()
    try:
        cursor = conn.cursor()

        # Таблица категорий (виды тестирования)
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS categories (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                name TEXT NOT NULL UNIQUE,
                slug TEXT NOT NULL UNIQUE,
                description TEXT,
                icon TEXT DEFAULT 'check-circle'
            )
        """)

        # Таблица тем/материалов
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS topics (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                category_id INTEGER NOT NULL,
                title TEXT NOT NULL,
                content TEXT NOT NULL,
                order_num INTEGER DEFAULT 0,
                FOREIGN KEY (category_id) REFERENCES categories(id) ON DELETE CASCADE
            )
        """)

        # Таблица тестов
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS quizzes (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                category_id INTEGER,
                title TEXT NOT NULL,
                description TEXT DEFAULT '',
                FOREIGN KEY (category_id) REFERENCES categories(id) ON DELETE SET NULL
            )
        """)

        # Таблица вопросов
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS questions (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                quiz_id INTEGER NOT NULL,
                question_text TEXT NOT NULL,
                option_a TEXT NOT NULL,
                option_b TEXT NOT NULL,
                option_c TEXT NOT NULL,
                option_d TEXT NOT NULL,
                correct_option TEXT NOT NULL CHECK(correct_option IN ('A','B','C','D')),
                explanation TEXT DEFAULT '',
                order_num INTEGER DEFAULT 0,
                FOREIGN KEY (quiz_id) REFERENCES quizzes(id) ON DELETE CASCADE
            )
        """)

        # Таблица результатов тестов
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS quiz_results (
                id TEXT PRIMARY KEY,
                quiz_id INTEGER NOT NULL,
                score INTEGER NOT NULL,
                total INTEGER NOT NULL,
                created_at TEXT NOT NULL,
                FOREIGN KEY (quiz_id) REFERENCES quizzes(id) ON DELETE CASCADE
            )
        """)

        # Таблица словаря терминов
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS glossary (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                term TEXT NOT NULL UNIQUE,
                definition TEXT NOT NULL,
                letter TEXT NOT NULL
            )
        """)

        # Таблица отзывов/комментариев
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS feedback (
                id TEXT PRIMARY KEY,
                name TEXT NOT NULL,
                email TEXT DEFAULT '',
                message TEXT NOT NULL,
                rating INTEGER DEFAULT 0,
                created_at TEXT NOT NULL
            )
        """)

        # Таблица прогресса пользователя (по сессии)
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS user_progress (
                id TEXT PRIMARY KEY,
                session_id TEXT NOT NULL UNIQUE,
                topics_read INTEGER DEFAULT 0,
                quizzes_passed INTEGER DEFAULT 0,
                total_score INTEGER DEFAULT 0,
                last_visit TEXT,
                UNIQUE(session_id)
            )
        """)

        # Таблица прочитанных тем (детальное отслеживание)
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS read_topics (
                session_id TEXT NOT NULL,
                topic_id INTEGER NOT NULL,
                read_at TEXT NOT NULL,
                PRIMARY KEY (session_id, topic_id)
            )
        """)

        # Таблица закладок
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS bookmarks (
                session_id TEXT NOT NULL,
                topic_id INTEGER NOT NULL,
                bookmarked_at TEXT NOT NULL,
                PRIMARY KEY (session_id, topic_id),
                FOREIGN KEY (topic_id) REFERENCES topics(id) ON DELETE CASCADE
            )
        """)

        # Таблица сессий администратора
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS admin_sessions (
                id TEXT PRIMARY KEY,
                username TEXT NOT NULL,
                expires TEXT NOT NULL,
                created_at TEXT NOT NULL
            )
        """)

        conn.commit()
    finally:
        conn.close()


