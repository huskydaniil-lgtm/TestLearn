@app.post("/quiz/{quiz_id}/submit")
async def quiz_submit(request: Request, quiz_id: int):
    """Обработка результатов теста с обновлением прогресса."""
    session_id = get_session_id(request)
    ensure_session_progress(session_id)

    conn = get_db()
    try:
        form_data = await request.form()

        questions = conn.execute("""
            SELECT * FROM questions WHERE quiz_id = ? ORDER BY order_num
        """, (quiz_id,)).fetchall()

        score = 0
        total = len(questions)
        results = []

        for q in questions:
            answer = form_data.get(f"q_{q['id']}", "").upper()
            is_correct = answer == q["correct_option"]
            if is_correct:
                score += 1
            results.append({
                "question": q["question_text"],
                "your_answer": answer if answer else "Без ответа",
                "correct_answer": q["correct_option"],
                "is_correct": is_correct,
                "explanation": q["explanation"],
                "option_a": q["option_a"],
                "option_b": q["option_b"],
                "option_c": q["option_c"],
                "option_d": q["option_d"],
            })

        # Сохраняем результат
        result_id = str(uuid.uuid4())
        conn.execute("""
            INSERT INTO quiz_results (id, quiz_id, score, total, created_at)
            VALUES (?, ?, ?, ?, ?)
        """, (result_id, quiz_id, score, total, datetime.now().isoformat()))

        quiz = conn.execute("SELECT * FROM quizzes WHERE id = ?", (quiz_id,)).fetchone()
        conn.commit()
    finally:
        conn.close()

    # Обновляем прогресс пользователя
    update_session_progress(session_id, "quizzes_passed")
    update_session_progress(session_id, "total_score", increment=score)

    percentage = int((score / total) * 100) if total > 0 else 0

    response = templates.TemplateResponse("quiz_result.html", {
        "request": request,
        "active_page": "quiz",
        "quiz": quiz,
        "score": score,
        "total": total,
        "percentage": percentage,
        "results": results,
    })
    set_session_cookie(response, session_id)
    return response


