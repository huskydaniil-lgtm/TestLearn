def format_content(text: str) -> str:
    """Преобразует простой markdown в HTML для отображения контента тем."""
    if not text:
        return ""
    html = text

    # Шаг 1: Заменяем **bold** → <strong>bold</strong>
    html = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', html)

    # Шаг 2: Разбиваем на блоки по двойному переводу строки
    blocks = re.split(r'\n\s*\n', html)
    result = []

    for block in blocks:
        block = block.strip()
        if not block:
            continue

        lines = block.split('\n')

        # Проверяем, является ли блок списком
        is_list = all(
            re.match(r'^(\d+)\.\s+.+$', l.strip()) or re.match(r'^-\s+.+$', l.strip())
            for l in lines if l.strip()
        )

        if is_list:
            items = []
            for line in lines:
                line = line.strip()
                m = re.match(r'^(\d+)\.\s+(.+)$', line)
                if m:
                    items.append(f'<li>{m.group(2)}</li>')
                else:
                    m = re.match(r'^-\s+(.+)$', line)
                    if m:
                        items.append(f'<li>{m.group(1)}</li>')
            if items:
                result.append(f'<ul>{"".join(items)}</ul>')
        else:
            # Обычный текстовый блок: одиночные \n → <br>
            block_html = '<br>'.join(l.strip() for l in lines if l.strip())
            result.append(f'<p>{block_html}</p>')

    return '\n'.join(result)


